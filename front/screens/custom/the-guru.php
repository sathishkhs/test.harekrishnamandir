              <h5>Who is a Guru?</h5>
              <p>A spiritual master is the representative of the Supreme Personality of Godhead, descended from the spiritual world to this material world for reclaiming the fallen souls. </p>
              <p>Everyone is in the darkness of ignorance. Therefore everyone needs to be enlightened with transcendental knowledge. One who enlightens his disciple and saves him from rotting in the darkness of ignorance in this material world is a true guru. The word guru means “heavy.” He is heavy with Vedic knowledge. </p>
              
            </div>
            <div class="col-sm-12">
                <h5>Need of a Spiritual Master</h5>
                <p>A guru should be approached not for any material benefit but only for the purpose of getting liberated from the pangs of material existence. It is explained in Srimad Bhagavatam (11.3.21)</p>
                <h5>tasmad gurum prapadyeta jijnasuh shreya uttamam<br>
                shabde pare ca nishnatam brahmany upashamashrayam</h5>
                <p>“Any person who seriously desires to achieve real happiness must seek out a bona fide spiritual master and take shelter of him by initiation. The qualification of the spiritual master is that he must have realized the conclusion of the scriptures by deliberation and be able to convince others of these conclusions. Such great personalities, who have taken shelter of the Supreme Godhead, leaving aside all material considerations, are to be understood as bona fide spiritual masters.”</p>
                
                
            </div>
            <div class="col-md-8">
                
                <h5>Confidentail associate of the Supreme Lord</h5>
                <p>A bona fide spiritual master is a very confidential associate of the Supreme Lord Sri Krishna. On the order of the Supreme Lord he becomes the spiritual master of the whole world and preaches the gospel of Krishna. He has no business other than teaching people about reestablishing their forgotten realtionship with the Lord and taking them back to Godhead. A bona fide spiritual master is not self made – he is authorized to become a spiritual master by the order of Lord Krishna.</p>
                <p>A guru is also known as 'Acharya', one who teaches by his own example. Varaha Puraan defines an acharya as, “One who knows the import of all Vedic literature, explains the purpose of the Vedas, abides by their rules and regulations, and teaches his disciples to act in the same way.” </p>
                <p>Srila Prabhupada is the 32nd spiritual master descending in the glorious 'Brahma Madhva Gaudiya Sampradaya', which begins from Lord Krishna Himself. </p>
                
