                <h5>Brahma Madhva Gaudiya Sampradaya</h5>
                <p>Hare Krishna Movement, established by HDG A.C. Bhaktivedanta Swami Prabhupada traces its roots in Brahma Madhva Gaudiya Sampradaya, headed by Lord Brahma. Lord Brahma is the foremost disciple of Lord Krishna. In Brahma Sampradaya, Sri Madhvacharya significantly contributed by writing a commentary on the Brahma-sutras. After this the sampradaya came to be known as the Brahma-Madhva-sampradaya. The Brahma-Madhva-sampradaya further spread to the present day West Bengal, which was known as Gauda-desha so the sampradaya got its name, Brahma-Madhva-Gaudiya-sampradaya.</p>

