                <p>The correct process of receiving knowledge in any field is by approaching a bona fide institute. Similarly, transcendental knowledge should be received through a bona fide disciplic succession.</p>
                <h5>Guru Parampara </h5>
                <p>The original source of all Vedic knowledge is Supreme Lord Sri Krishna. From Him the Vedic knowledge has been passed on through the generations of pure devotees, who carefully carried this message without diluting or adulterating its essence. The authorized chain of spiritual master and disciple who carry transfer this transcendental knowledge while meticulously manitaining it purity and import is known as 'Guru Parampara'. </p>

