                <p>There are four authorized vaishnava sampradayas or schools of spirituality as enjoined in the Vedic scriptures. These four sampradayas originate from the Supreme Lord Sri Krishna. Lord Krishna imparted the divine knowledge to His four significant devotees, Lord Brahma, Lord Siva, Goddess Lakshmi and the four Kumaras from whom the sampradayas emerged. They further disseminated the received knowledge to their disciples and in this way the paramparas have to being. </p>


