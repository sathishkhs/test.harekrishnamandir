                <p>In the Srimad Bhagavatam it is said: gavah panca pavitrani: the cows deliver five products and all of them are pure and uncontaminated. The five products received from the cow are milk, ghee, yogurt, cow dung and urine. These five products, called pancha-gavya, are required in all ritualistic ceremonies performed according to the Vedic directions.</p>
                <p>In Vedic tradition, cows are worshipped as mothers. Just as the child feeds on the milk of the mother, human society takes milk from the cow. Srila Prabhupada always emphasized that the human society should recognize the importance of the cow and the bull and thus give all protection to these important animals. </p>
