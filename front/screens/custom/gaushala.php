              <p>The Supreme Lord Sri Krishna is worshipped thus: "My Lord, You are the well-wisher of the Cows and the brahmanas, and You are the well-wisher of the entire human society and world." (Visnu Purana 1.19.65)</p>
              <p>Srila Prabhupada says, "Lord Krishna, the Supreme Personality of Godhead, is the prime protector of brahminical culture and the Cow. Without knowing and respecting these pleasing personalities, one cannot realize the science of God, and without this knowledge, any welfare activities or humanitarian propaganda cannot be successful." </p>
