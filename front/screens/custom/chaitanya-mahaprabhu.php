              <h5>Who is Chaitanya Mahaprabhu?</h5>
              <p>Five thousand years ago, Lord Krishna appeared on earth for reestablishing the lost tenets of religion. He gave the message Bhagavad-gita for the people to attain liberation from the clutches of material existence and reach His eternal blissful abode. His final and most important instruction in the Gita was, “sarva dharman parityajya mam ekam sharanam vraja”, meaning “Abandon all varieties of religion and just surrender unto Me.”</p>
              
            </div>
            <div class="col-sm-12">
                <p>Lord Krishna gave this message at the end of Dvapara-yuga, and gradually Kali-yuga began. In this Kali-yuga people were not so qualified to understand and implement this message of surrender. So, the Lord compassionately appeared 500 years ago in Bengal as Chaitanya Mahaprabhu, and practically taught how to surrender to the Supreme Personality of Godhead Lord Krishna. In other words, Lord Krishna, by His own demonstration, in the form of Chaitanya, taught people how to surrender unto Him.</p>
                 
                
            </div>
            <div class="col-md-8">
           
                <p>Chaitanya Mahaprabhu appeared on 18th February, 1486 during the night of full Lunar eclipse. Historically He is known as the founder of ‘bhakti movement’ in Bengal. Lord Chaitanya Mahaprabhu gave the simplest way of surrendering unto Lord Krishna by introducing the process of chanting of Krishna’s holy names. He is the inaugurator of the sankirtana movement, which spread like wild fire in Bengal and Orissa. Later Lord Chaitanya personally propagated in other parts of the country. Sankirtana refers to congregational chanting of the holy names of the Lord. </p>
                <p>Philosophy of Sri Chaitanya Mahaprabhu is to remain in whatever position we are and simply chant the Hare Krishna Maha-mantra. This will not only liberate us from the mires of material existence but also give us the rarest and highest perfection of human life, “Love of Krishna”..</p>

