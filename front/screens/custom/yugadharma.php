                    <p >The Vedic scriptures inform that the chanting of this Hare Krishna Maha-mantra composed of sixteen words is the process of yoga for this age of Kali. </p>
                    <p >As per the Vedic scriptures there are four ages or 'yugas' known as the Satya-yuga, Treta-yuga, Dwapar-yuga & Kali-yuga. Just as the calendar months or the seasons keep changing in rotation, similarly the four yugas also change in rotation. </p>
                    <p >The cycle of Satya is characterized by virtue, wisdom and religion, there being practically no ignorance and vice. In the Treta-yuga vice is introduced. In the Dvapara-yuga there is an even greater decline in virtue and religion. And finally in Kali-yuga (the present yuga) there is an abundance of strife, ignorance, irreligion and vice. The cycle of the four yugas, namely Satya, Treta, Dvapara and Kali, rotates like the calendar months.</p>
