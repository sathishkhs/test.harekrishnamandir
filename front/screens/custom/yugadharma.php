            <div class="col-sm-12">
                <p>For each yuga there is a prescribed method of self realization called yugadharma. The word yuga means age and dharma means the path of religion. Srimad-Bhagavatam (12.3.52) explains:</p>
                <h6>krte yad dhyayato visnum tretayam yajato makhaih <br>
dvapare paricaryayam kalau tad dhari-kirtanat</h6>
<p>“Whatever result was obtained in Satya-yuga by meditating on Vishnu, in Treta-yuga by performing sacrifices and in Dvapara-yuga by serving the Lord’s lotus feet can also be obtained in Kali-yuga simply by chanting the Hare Krishna maha-mantra.” It is very difficult to meditate in this disturbing age of Kali. In the Satya-yuga, when people used to live for one hundred thousand years, Valmiki Muni attained perfection by meditating for sixty thousand years. But now we have no guarantee that we are going to live for sixty years or even sixty hours. So meditation is not possible in this age. In the Treta-yuga, people performed rituals, as they are described in the Vedic shastra. That requires huge amounts of money. In the present age people are very poor, so they cannot perform these sacrifices. dvapare paricaryayam—in the Dvapara-yuga it was possible to worship the Deity opulently in the temple, but nowadays, in the Kali-yuga, that is also an impossible task. Therefore, the general recommendation is kalau tad dhari-kirtanat: in this age of Kali one can attain all perfection simply by chanting the holy name of the Lord.</p>
<p>
It is enjoined in the Kali-santarana Upanishad:
</p>  
<h6>hare krishna hare krishna krishna krishna hare hare
hare rama hare rama rama rama hare hare
iti sodasakam namnam kali-kalmasa-nasanam
natah parataropayah sarva-vedesu drsyate</h6>          
<p>“Hare Krishna, Hare Krishna, Krishna Krishna, Hare Hare/ Hare Rama, Hare Rama, Rama Rama, Hare Hare. These sixteen names composed of thirty-two syllables are the only means of counteracting the evil effects of the Kali-yuga. After searching through all the Vedic literature, one cannot find a method of religion for this age so sublime as the chanting of the Hare Krishna mantra”.</p>
