              <p>Krishna consciousness means to work in full knowledge of one's relationship with the Supreme Absolute, and the perfection of this consciousness is full knowledge of Krishna, or the Supreme Personality of Godhead. A pure soul is the eternal servant of God as His fragmental part and parcel. We are all meant to serve the Lord in a transcendental loving relationship in the spiritual world. When we refuse to serve the Supreme Lord and become rebellious towards Him, we are put into this material world. Due to the desire to lord it over material nature one undergoes many sufferings.</p>
              
