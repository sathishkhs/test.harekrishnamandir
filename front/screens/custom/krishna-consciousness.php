            <div class="col-sm-12">
                <p>Dharma means to abide by the laws of God. Just like, a good citizen is he, who knows his relationship with the state and abides by the laws of the state. One who doesn’t care for or abides by the state laws is called as a bad citizen or a criminal. Such citizens are put into the prison house for reformation. Similarly, the living entities are part and parcel of God. But when they are not ready to abide by the laws of God, they are put into this material world for reformation. The process of reformation and reestablishment of the living entities lost relationship with the Supreme Lord is called yoga. Krishna consciousness is that process of yoga.</p>
                <p>In this imprisoned life of material world we are revolving in the cycle of 8,400,000 species and thus suffering in many ways. The human form of life is a chance to get out of this imprisoned life, and as such the only occupation of the human being is to reestablish his lost relationship with God.</p>
                 
                
            </div>
            <div class="col-md-8">
           
                <p>Krishna consciousness is a direct process of linking ourselves with the Supreme Lord by engaging in His loving devotional service with the proper understanding of His position and His relationship with us. “Religion without philosophy is sentiment and philosophy without religion is speculation.” Therefore, in order to establish relationship with the Lord one must have proper philosophical understanding of the Lord based on the bona fide Vedic scriptures and one must engage in practical religious actions as taught by the scriptures and great acharyas. Knowledge guides us in proper action and action helps us in realizing the spiritual knowledge. Krishna consciousness stands on these two fundamental principles and thus, is an authorized process of yoga. We should endeavor, therefore, to organize our lives in such a way as to follow Krishna’s teachings as far as possible. Lord Krishna promises that we will be happy not only in this life but also in the next..</p>
                
