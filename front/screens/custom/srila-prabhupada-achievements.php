                            <p>His Divine Grace Srila Prabhupada was born in 1896 in Calcutta, India. Upon seeing Srila Prabhupada’s horoscope an astrologer once predicted that he would build a house in which whole world can live. The inspiration for this came when Srila Prabhupada met his spiritual master Srila Bhaktisiddhanta Saraswati Thakura in 1922. This instruction was to preach the message of Krishna to the western world. The message repeated as the final word from the spiritual master in 1936, which Srila Prabhupada took as his life and soul. He spent three decades in trying to fulfill his guru’s order and finally succeeded in 1965 when he reached America. </p>
