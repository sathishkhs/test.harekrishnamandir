              <h3 class="mt-0">Bhagavad-Gita</h3>
              <h5>What is the Bhagavad-gita?</h5>
              <p>General understanding of the masses is that Bhagavad-gita is a holy book of Hindu religion, an armchair book for the retirement years or an ancient scripture full of philosophical intricacies. All these opinions make the Gita more or less outdated in the modern context. But by learning from the authorized sources, one understands that Bhagavad-gita is much more than this: it is a guide book to lead an enjoyable and blissful life. In other words, it is a user’s manual to lead a meaningful human life.</p>
              
            </div>
            <div class="col-sm-12">
                <p>Lord Krishna imparted this knowledge to Arjuna in the battlefield of Kurukshetra, when the latter was in a distressful condition. Upon receiving this knowledge returned to his happy and stable position. All of us undergo difficult situations in life, and often become overwhelmed by those situations not knowing where to find a solution. Bhagavad-gita guides us out of such delusions and reestablishes us in our origin position of eternity, knowledge and bliss. If we follow the instructions as given by Lord Krishna, then we can attain the same blissful state as Arjuna did.</p>
                 
                
            </div>
            <div class="col-md-8">
                <h5>How relevant is it in modern times?</h5>
                <p>Everyone wants to be happy and everyone is constantly searching for that happiness. Whether a child or an old-man, an Indian or an American, Hindu or Christian, man or woman, everyone is looking for happiness. But unfortunately, for the want of real knowledge of happiness they are searching for it everywhere and getting frustrated. Bhagavad-gita shows us where that happiness is. Just like when we purchase a new gadget, a user’s manual comes with it. The manual teaches us how to make the best use of that gadget and derive maximum happiness out of it. For the want of proper knowledge we may invent our own ways of using it but finally we will become frustrated. So, Bhagavad-gita is manual given by the Supreme Lord Krishna which guides us in making the best use of this human life and to deriving real happiness from it.</p>
                <p>In the modern times when people are more confused and misdirected, the Gita become all the more relevant for the individual as well as the society at large in bringing back the stability and happiness in life..</p>

